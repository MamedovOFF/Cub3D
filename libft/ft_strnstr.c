#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	if (little && 0 == little[0])
		return ((char *)big);
	while (big[i] && i < len)
	{
		if (big[i] == little[0])
		{
			j = 0;
			while (big[i + j] == little[j] && (i + j) < len
				&& big[i + j] && little[j])
				j++;
			if (j == ft_strlen(little))
				return ((char *)(big + i));
		}
		i++;
	}
	return ((void *) 0);
}
