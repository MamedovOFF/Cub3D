#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*new_s;
	unsigned int	i;

	if (!s)
		return ((void *)0);
	i = 0;
	new_s = (char *) malloc (ft_strlen(s) + 1);
	if ((void *)0 == new_s)
		return ((void *)0);
	while (s[i])
	{
		new_s[i] = f(i, s[i]);
		i++;
	}
	new_s[i] = 0;
	return (new_s);
}
